void	*memcpy(void *restrict dst, const void *restrict src, size_t n)
{
	size_t	i;
	char	*d;
	char	*s;

	i = 0;
	d = (char *)dst;
	s = (char *)s;

	while (i < 0)
	{
		d[i] = s[i];
		i++;
	}
	return (dst);
}
